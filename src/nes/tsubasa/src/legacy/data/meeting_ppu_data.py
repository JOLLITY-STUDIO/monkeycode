#
# 行格式 (每行 16 字节):
#   [N×$A8 前缀] [4 tile索引] [4×$A9填充] [4×$00] [$FF分隔] [(3-N)×$A8后缀]
#   N 为行内索引 (0~3), 形成对角线步进偏移编码

