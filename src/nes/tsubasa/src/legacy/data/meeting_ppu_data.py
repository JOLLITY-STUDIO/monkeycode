# ============ 辅助函数 ============

def catMeeting(scene=None):
    """从场景 tilemap 中提取 MEETING 区的 tile 索引。
    
    行结构: [N×$A8前缀] [4 tiles] [4×$A9] [4×$00] [$FF] [(3-N)×$A8]
    返回: (row0_tiles, row1_tiles, row2_tiles, row3_tiles) 四个元组
    """
    if scene is None:
        scene = SCENE_TILEMAP_RAW
    # MEETING 4 行在场景偏移 0x60-0x9F
    rows = []
    for i in range(4):
        off = 0x60 + i * 16
        # tiles 在位置 i..i+3 (因为 N 个 A8 前缀)
        tiles = scene[off + i : off + i + 4]
        rows.append(bytes(tiles))
    return tuple(rows)

def catMeetingSeq(scene=None):
    """提取 MEETING 连续 tile 序列: M E E T I N G"""
    rows = catMeeting(scene)
    # row2=MEET (4 tiles), row3=ING (取前 3 tiles, 末尾 $00 是填充)
    return bytes([*rows[2], *rows[3][:3]])


