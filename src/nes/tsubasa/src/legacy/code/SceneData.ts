/**
 * $E8-$FF 控制操作码枚举
 */
export enum CtrlOpcode {
  SET_PPU_ADDR = 0xE8,    // 直接设 PPU 地址
  WAIT_2FRAMES = 0xE9,    // 等 2 帧 + 清输入
  CLEAR_SCREEN = 0xEA,    // 清屏 + 开 PPU + 双 nametable 填充
  PPU_OFF = 0xEB,         // 关 PPU + 动画回收
  CONDITION = 0xEC,       // 条件判断 ($1C 场景子状态)
  SCENE_QUEUE = 0xED,     // 场景队列写
  UI_PANEL = 0xEE,        // 写 UI 面板 ($220B)
  TOGGLE_SOUND = 0xEF,    // 切换音效标记
  LOAD_DATA_BLOCK = 0xF0, // 加载新数据块
  UI_PANEL_2 = 0xF1,      // 写 UI 面板 2
  PLAY_MUSIC = 0xF2,      // 播放音乐
  SET_ATTR_HI = 0xF3,     // 设属性表高字节
  WAIT_INPUT = 0xF4,      // 等待输入
  SUB_OP_F5 = 0xF5,       // F5 子操作码分发
  SET_SPRITE_Y = 0xF6,    // 设精灵 Y
  DELAY_PARAM = 0xF7,     // 参数帧延时
  TOGGLE_SCROLL = 0xF8,   // 滚屏开关
  ENTER_MATCH = 0xF9,     // 进入比赛引擎
  PLAY_SFX_SCROLL = 0xFA, // 播放音效 + 滚屏标志
  PLAY_SFX_SCROLL2 = 0xFB,// 播放音效 + 滚屏标志2
  LOAD_PALETTE = 0xFC,    // 加载调色板
  SCROLL_ADVANCE = 0xFD,  // 滚屏推进 $40
  PPU_OFF_ADVANCE = 0xFE, // 关 PPU + 延迟 + 前进
  SCENE_END = 0xFF,       // 场景结束/跳转
}
