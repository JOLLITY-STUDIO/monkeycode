const content = fs.readFileSync('src/tsnes/tsubasa-code/prg_banks/bank_0.ts', 'utf8');
const match = content.match(/const _PRG_BANK_0.*?=\s*\[([\s\S]*?)\]/);
const bytes = (match?.[1]?.match(/0x[0-9a-fA-F]{2}/g) ?? []).map(h => parseInt(h, 16));
console.log(`PRG Bank 0 - ${bytes.length} bytes (16KB) mapped at $8000-$BFFF`);
console.log('');

const fmt = (n, w) => n.toString(16).toUpperCase().padStart(w, '0');
const BASE = 0x8000;
let pc = 0, count = 0;

// Special addresses we want to annotate
const KNOWN_JMP_TABLE = 0x800D;  // 0x800D-0x8018 = jump table
const RESET_VECTOR = 0x8019;

while (pc < bytes.length) {
  const addr = BASE + pc;
  const opkey = fmt(bytes[pc], 2);
  const rec = OPCODE_TABLE[opkey.toLowerCase()];

  // Handle non-code data regions
  if (!rec) {
    let end = pc + 1;
    while (end < bytes.length && end - pc < 16 && !OPCODE_TABLE[fmt(bytes[end], 2).toLowerCase()]) {
      end++;
    }
    const chunk = bytes.slice(pc, end);
    const hex = chunk.map(b => fmt(b, 2)).join(' ');
    console.log(`; $${fmt(addr,4)} ${hex.padEnd(48)} ; DATA ${chunk.length} bytes`);
    pc = end;
    count++;
    continue;
  }

  const [ins, mode, size] = rec;
  const insName = INS_NAMES[ins] ?? `???${ins}`;
  let operand = '';

  if (size >= 2) {
    const lo = bytes[pc + 1];
    if (mode === 1) {
      const t = addr + 2 + (lo < 128 ? lo : lo - 256);
      operand = `$${fmt(t, 4)}`;
    } else if (mode === 5) {
      operand = `#$${fmt(lo, 2)}`;
    } else if (size === 2) {
      if (mode === 10) operand = `($${fmt(lo, 2)},X)`;
      else if (mode === 11) operand = `($${fmt(lo, 2)}),Y`;
      else operand = `$${fmt(lo, 2)}`;
    }
  }
  if (size === 3) {
    const lo = bytes[pc + 1], hi = bytes[pc + 2];
    const abs = lo | (hi << 8);
    if (mode === 12) operand = `($$${fmt(abs, 4)})`;
    else {
      operand = `$${fmt(abs, 4)}`;
      if (mode === 8) operand += ',X';
      if (mode === 9) operand += ',Y';
    }
  }
  if (mode === 4) operand = 'A';

  const hex = bytes.slice(pc, pc + size).map(b => fmt(b, 2)).join(' ');

  // Special sections
  if (addr === KNOWN_JMP_TABLE) {
    console.log('');
    console.log('; ====== JUMP TABLE ($800D-$8018) - 6 pointers = (lo,hi) pairs ======');
  }
  if (addr === RESET_VECTOR) {
    console.log('');
    console.log('; ====== RESET ENTRY ($8019) - main game init ======');
    console.log('');
  }

  console.log(`$${fmt(addr,4)} ${hex.padEnd(9)} ${insName.padEnd(4)} ${operand}`);

  pc += size;
  count++;
}

console.log('');
console.log(`=== Total: ${count} lines, ${bytes.length - pc} trailing bytes ===`);
