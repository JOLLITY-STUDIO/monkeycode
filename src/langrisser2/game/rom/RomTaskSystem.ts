export class RomTaskSystem extends RomProgram {

  private phase: Phase = Phase.BOOTING;
  private step: number = 0;
  private frameCount: number = 0;
  private subPhase: number = 0;
  private runningTask: number = 0;

  /** 显示队列处理器 (共用 ROM data/ram/vdp) */
  readonly displayQueue: RomDisplayQueue;

  // C944 子状态
  private c944Step: C944_Step = C944_Step.INIT;
  private c944SubFrame: number = 0;

  // C9AA 子状态
  private c9aaStep: C9AA_Step = C9AA_Step.INIT;

  // ★ Attract/Demo 模式跟踪变量
  /** 标题画面空闲帧计数 (无输入时递增, 有输入时清零) */
  private titleIdleFrames: number = 0;
  /** 当前是否处于 demo 播放模式 */
  private inDemoMode: boolean = false;
  /** 上一次检测到的 joypad 输入值 (用于上升沿检测) */
  private lastInputValue: number = 0;
  /** $FF78FA 上一次被检测到的值 */
  private lastDemoFlag: number = 0xFFFF;
  /** demo 模式内帧计数 */
  private demoFrameCount: number = 0;

  // ★ 标题菜单状态变量
  /** 菜单选项: 0=NEW GAME, 1=CONTINUE, 2=OPTIONS */
  private titleMenuCursorIndex: number = 0;
  /** 光标闪烁帧计数器 */
  private titleMenuBlinkFrame: number = 0;
  /** 菜单是否已初始化过 (一次性 setup) */
  private titleMenuSetupDone: boolean = false;
  /** 菜单项数量 */
  private readonly TITLE_MENU_ITEM_COUNT = 3;
  /** 菜单项屏幕 Y 坐标 (像素, 不含 128 偏移) */
  private readonly TITLE_MENU_Y: readonly number[] = [148, 166, 184];
  /** 菜单位置 X (像素, 不含 128 偏移) */
  private readonly TITLE_MENU_X = 104;

  constructor(romData: Uint8Array, ram: Ram, vdp: VdpChip) {
    super(romData, ram, vdp);
    this.displayQueue = new RomDisplayQueue(romData, ram, vdp);
  }

  // ============================================================
  // 生命周期
  // ============================================================

  markInitsDone(): void {
    this.phase = Phase.GAME_INIT;
    this.step = 0;
    console.log('[TaskSystem] 初始化链启动: C80C→1C834→1C854→阶段分派→C92C→C93A→C944→C9AA→CA00');
  }

  frameUpdate(): void {
    this.frameCount++;

    switch (this.phase) {
      case Phase.BOOTING:
        return;
      case Phase.GAME_INIT:
        this._runInitChain();
        break;
      case Phase.RUNNING:
        this._dispatchRunningTask();
        break;
    }

    // ★ 每帧处理显示队列 (CRAM DMA / VRAM 数据传输)
    this.displayQueue.processDisplayQueue();
  }

  // ============================================================
  // ★ 输入 / Demo 跟踪辅助
  // ============================================================

  /**
   * 读取当前 joypad 字节 ($FFFF8179)
   * 位: 0=↑ 1=↓ 2=← 3=→ 4=B 5=C 6=A 7=START
   */
  private _readJoypadState(): number {
    return this.ram.read8(0xFF8179);
  }

  /**
   * 检查是否有新输入 (上升沿检测, 用于重置 demo 倒计时)
   */
  private _hasNewInput(): boolean {
    const current = this._readJoypadState();
    const hasNew = current !== 0 && current !== this.lastInputValue;
    this.lastInputValue = current;
    return hasNew;
  }

  /**
   * 跟踪 $FF78FA 的变化 (demo 标志位监控)
   */
  private _trackDemoFlag(): void {
    const current = this.ram.read16(RAM_DEMO_FLAG);
    if (current !== this.lastDemoFlag) {
      console.log(
        `[Demo] $FF78FA 变化: 0x${this.lastDemoFlag.toString(16).padStart(4, '0').toUpperCase()} ` +
        `→ 0x${current.toString(16).padStart(4, '0').toUpperCase()} (帧${this.frameCount})`
      );
      this.lastDemoFlag = current;
    }
  }

  /**
   * 标题画面空闲倒计时检测 & Demo 模式触发
   * ROM $C9BC: tst.w ($FF78FA); bne.w $CA3C
   *
   * @returns true 如果应进入 demo 模式
   */
  private _checkTitleIdleTimeout(): boolean {
    const hadInput = this._hasNewInput();

    if (hadInput) {
      if (this.titleIdleFrames > 0) {
        console.log(
          `[Demo] 检测到输入 (joypad=0x${this.lastInputValue.toString(16).padStart(2, '0')}) ` +
          `→ 重置空闲计时 (帧${this.frameCount})`
        );
      }
      this.titleIdleFrames = 0;
      this.ram.write16(RAM_DEMO_FLAG, 0x0000);
      return false;
    }

    // 无输入 → 递增
    this.titleIdleFrames++;

    if (this.titleIdleFrames >= TITLE_IDLE_TIMEOUT) {
      console.log(
        `[Demo] ★ 空闲 ${TITLE_IDLE_TIMEOUT} 帧 (~${Math.round(TITLE_IDLE_TIMEOUT / 60)}秒) ` +
        `无输入 → 进入 Demo/Attract 模式! (帧${this.frameCount})`
      );
      this.ram.write16(RAM_DEMO_FLAG, 0xFFFF);
      this.titleIdleFrames = 0;
      return true;
    }

    if (this.titleIdleFrames % TITLE_LOG_INTERVAL === 0 && this.titleIdleFrames > 0) {
      const secondsLeft = Math.round((TITLE_IDLE_TIMEOUT - this.titleIdleFrames) / 60);
      console.log(
        `[Demo] 空闲计时: ${this.titleIdleFrames}/${TITLE_IDLE_TIMEOUT} 帧 ` +
        `(${Math.round(this.titleIdleFrames/60)}s / 约${secondsLeft}s后进入demo)`
      );
    }

    return false;
  }

  // ================================================================
  // ★ 标题菜单: 光标移动 + 选择
  // (TS 端实现, ROM 等效: $CA0A)
  // ================================================================

  /**
   * 标题菜单主处理器 — 每帧调用
   * 处理: 方向键移动光标, A/Start 确认, B 返回
   */
  private _handleTitleMenu(): void {
    // 首次进入菜单 — 初始化光标
    if (!this.titleMenuSetupDone) {
      this._setupTitleMenuCursor();
      this.titleMenuSetupDone = true;
      console.log('[Menu] ★ 标题菜单激活: ↑↓ 移动光标, A/START 确认, B 返回');
    }

    const joypad = this._readJoypadState();
    const prevJoypad = this.lastInputValue;

    // 持续按住方向键需要去抖 (每 10 帧移一次)
    const canMove = (this.titleMenuBlinkFrame % 10 === 0);

    // 上方向
    if (canMove && (joypad & 0x01)) {
      this.titleMenuCursorIndex = (this.titleMenuCursorIndex - 1 + this.TITLE_MENU_ITEM_COUNT) % this.TITLE_MENU_ITEM_COUNT;
      this._updateTitleMenuCursorSprite();
      console.log(`[Menu] 光标 → ${this._menuItemName(this.titleMenuCursorIndex)}`);
    }
    // 下方向
    if (canMove && (joypad & 0x02)) {
      this.titleMenuCursorIndex = (this.titleMenuCursorIndex + 1) % this.TITLE_MENU_ITEM_COUNT;
      this._updateTitleMenuCursorSprite();
      console.log(`[Menu] 光标 → ${this._menuItemName(this.titleMenuCursorIndex)}`);
    }

    // 确认 (A 或 Start) — 上升沿检测
    const newlyPressed = (joypad & ~prevJoypad) & 0xC0; // bit6=A, bit7=START
    if (newlyPressed) {
      const item = this.titleMenuCursorIndex;
      console.log(`[Menu] ★ 选择: ${this._menuItemName(item)}`);
      this._onTitleMenuSelect(item);
      return;
    }

    // 闪烁
    this.titleMenuBlinkFrame++;
    const visible = (Math.floor(this.titleMenuBlinkFrame / 30) & 1) === 0;
    this._setTitleMenuCursorVisible(visible);

    this.lastInputValue = joypad;
  }

  /** 菜单项名称映射 */
  private _menuItemName(idx: number): string {
    return ['NEW GAME', 'CONTINUE', 'OPTIONS'][idx] ?? `ITEM_${idx}`;
  }

  /** 菜单选择处理 */
  private _onTitleMenuSelect(idx: number): void {
    this._hideTitleMenuCursor();
    this.ram.write16(RAM_VARS.GAME_PHASE, 5);

    switch (idx) {
      case 0: // NEW GAME
        console.log('[Menu] ★ NEW GAME 被选择 → 进入游戏部署 CAF2/CA00');
        break;

      case 1: // CONTINUE
        console.log('[Menu] ★ CONTINUE 被选择 → 读取存档 (暂未实现, 进入 CAF2)');
        break;

      case 2: // OPTIONS
        console.log('[Menu] ★ OPTIONS 被选择 → 配置菜单 (暂未实现)');
        // 暂不跳转, 留在菜单
        return;
    }

    // ★ 转移到 SET_PHASE (由下一帧 C9AA dispatch 处理, 会写 TASK_PTR=CA00)
    this.c9aaStep = C9AA_Step.SET_PHASE;
  }

  // --- 光标精灵渲染 ---

  /**
   * 首次进入菜单 — 把箭头 tile 写入 VRAM 并初始化 SAT 精灵
   */
  private _setupTitleMenuCursor(): void {
    // ★ 确保 VDP 寄存器 5 (SAT 基地址) 已设置
    // 使用 Genesis 常见值: VRAM $D800 (reg $05 = $6C)
    this.vdp.writeRegister(0x05, 0x6C);

    // ★ 写箭头 tile 到 VRAM
    const CURSOR_TILE_INDEX = 0x01; // tile #1, VRAM $0020
    this.vdp.vram.writeBytes(CURSOR_TILE_INDEX * 32, TITLE_MENU_ARROW_TILE);

    // ★ 第一个精灵槽 (VRAM $D800)
    const SAT_ADDR = 0xD800;
    // Sprite entry (8 bytes):
    //   [0] Y low, [1] size/link, [2] link, [3] attrib high
    //   [4-5] tile+flags, [6] X low, [7] X high
    const entries: number[] = [];
    // Sprite 0: 箭头 (Y 在 _updateTitleMenuCursorSprite 中设置)
    entries.push(0x01, 0x00, 0x00, 0x0F, 0x00, CURSOR_TILE_INDEX, 0x00, 0x00);
    this.vdp.vram.writeBytes(SAT_ADDR, new Uint8Array(entries));
  }

  /**
   * 更新光标精灵 Y 坐标到当前菜单项
   */
  private _updateTitleMenuCursorSprite(): void {
    const y = this.TITLE_MENU_Y[this.titleMenuCursorIndex];
    const genesisY = y + 128; // Genesis VDP: screenY + 128
    const SAT_ADDR = 0xD800;
    this.vdp.vram.writeBytes(SAT_ADDR + 0, new Uint8Array([genesisY & 0xFF]));
    this.vdp.vram.writeBytes(SAT_ADDR + 6, new Uint8Array([this.TITLE_MENU_X + 128]));
  }

  /**
   * 设置光标可见性 (闪烁: 把 Y 坐标设为 0 或正常值)
   */
  private _setTitleMenuCursorVisible(visible: boolean): void {
    const SAT_ADDR = 0xD800;
    if (visible) {
      const y = this.TITLE_MENU_Y[this.titleMenuCursorIndex] + 128;
      this.vdp.vram.writeBytes(SAT_ADDR + 0, new Uint8Array([y & 0xFF]));
    } else {
      this.vdp.vram.writeBytes(SAT_ADDR + 0, new Uint8Array([0x00])); // Y=0 → 隐藏
    }
  }

  /** 隐藏菜单光标 */
  private _hideTitleMenuCursor(): void {
    const SAT_ADDR = 0xD800;
    this.vdp.vram.writeBytes(SAT_ADDR + 0, new Uint8Array([0x00]));
    this.vdp.vram.writeBytes(SAT_ADDR + 2, new Uint8Array([0x00]));
  }

  // ================================================================
  // GAME_INIT: 初始化任务链
  // ================================================================

  private _runInitChain(): void {
    switch (this.step) {
      case 0:
        console.log('[TaskSystem] step0: C80C→手动队列 1C834');
        this.ram.write32(RAM_VARS.TASK_PTR, TASK_1C834);
        this.step = 1;
        break;

      case 1:
        this._transl_1C834();
        this.step = 2;
        break;

      case 2:
        this._transl_1C854();
        break;
    }
  }

  // ================================================================
  // 1C834: DMA flag + 队列 1C854
  // ================================================================

  private _transl_1C834(): void {
    this.ram.write16(0xFF95A6, 1);
    this.ram.write16(0xFF95A8, 1);
    this.ram.write32(RAM_VARS.TASK_PTR, TASK_1C854);
    console.log('[TaskSystem] 1C834: 队列 1C854 (阶段分派器)');
  }

  // ================================================================
  // 1C854: 阶段分派器
  // ================================================================

  private _transl_1C854(): void {
    this.subPhase = this.ram.read16(PHASE_INDEX);
    console.log(`[TaskSystem] 1C854: 阶段分派, $FFAE90=${this.subPhase}`);

    if (this.subPhase < PHASE_COUNT) {
      const handlerAddr = PHASE_TABLE_OFFSETS[this.subPhase];
      this._dispatchPhaseHandler(this.subPhase, handlerAddr);
    } else {
      console.log(`[TaskSystem]  阶段越界 → 进入 RUNNING`);
      this._enterRunning();
    }
  }

  private _dispatchPhaseHandler(index: number, _addr: number): void {
    if (index === 0) {
      this._phase0_titleInit();
    }

    this.ram.write16(PHASE_INDEX, index + 1);

    if (index >= PHASE_COUNT - 1) {
      this._enterRunning();
    } else {
      this.ram.write32(RAM_VARS.TASK_PTR, TASK_1C854);
    }
  }

  private _phase0_titleInit(): void {
    const charTableBase = 0xFFFFA4CC;
    let classCount = 0;
    for (let i = 0; i < 10; i++) {
      const classId = this.ram.read8(charTableBase + i * 24 + 0x10);
      if (classId >= 2) classCount++;
    }
    this.ram.write16(0xFFAE9E, classCount);
    console.log(`[TaskSystem] 阶段0: ${classCount} 职业可用`);
  }

  private _enterRunning(): void {
    console.log('[TaskSystem] 初始化完成 → RUNNING, 任务=C92C');
    this.ram.write32(RAM_VARS.TASK_PTR, TASK_C92C);
    this.runningTask = TASK_C92C;
    this.phase = Phase.RUNNING;
  }

  // ================================================================
  // RUNNING: 任务分发
  //   C92C → C93A → C944 → C9AA → CA00
  // ================================================================

  private _dispatchRunningTask(): void {
    const taskFunc = this.ram.read32(RAM_VARS.TASK_PTR);
    if (taskFunc === 0) return;

    switch (taskFunc) {
      case TASK_C92C: this._transl_C92C(); break;
      case TASK_C93A: this._transl_C93A(); break;
      case TASK_C944: this._transl_C944(); break;
      case TASK_C9AA: this._transl_C9AA(); break;
      case TASK_CA00: this._transl_CA00(); break;
      default:
        if (this.frameCount % 60 === 0) {
          console.log(`[TaskSystem] 未知任务 $${taskFunc.toString(16)}`);
        }
        break;
    }
  }

  // ================================================================
  // C92C: fourcc 校验 → 队列 C93A
  // ROM: 在 C92C 函数末尾写入 C93A 到 TASK_PTR
  // ================================================================

  /**
   * ROM 等效:
   *   设 $FF95A2 = 0x5DF40 (标题参数指针)
   *   设 $FFA6D4 = 0 (TITLE_FLAG)
   *   设 $FFA49C = 1 (SCENE_TITLE)
   *   队列 C93A
   */
  private _transl_C92C(): void {
    this.ram.write32(0xFF95A2, TITLE_PARAM_PTR);
    this.ram.write16(0xFFA6D4, 0);
    this.ram.write16(0xFFA49C, SCENE_TITLE);

    this.ram.write32(RAM_VARS.TASK_PTR, TASK_C93A);
    this.runningTask = TASK_C93A;

    console.log('[TaskSystem] C92C→C93A: 标题画面进入');
  }

  // ================================================================
  // C93A: 标题画面入口 → 队列 C944
  // ROM $32977: move.l #$C944,(TASK_PTR); rts
  // ================================================================

  /** ROM $32978: move.l #$C944,(TASK_PTR); rts */
  private _transl_C93A(): void {
    this.ram.write32(RAM_VARS.TASK_PTR, TASK_C944);
    this.runningTask = TASK_C944;
    this.c944Step = C944_Step.INIT;
    this.c944SubFrame = 0;

    console.log('[TaskSystem] C93A→C944: 标题画面初始化');
  }

  // ================================================================
  // C944: 标题画面设置 (多步骤, ROM $32982-$33015)
  //
  // ROM 分三段执行 (由 TASK_PTR 队列自动推进):
  //   第1段: clr.w $A6D4 → clr.w $816E → jsr $942A → jsr $C7F6 → jsr $2D04C
  //   第2段: bsr $FCCE → jsr $942A → clr.b $AA10 → jsr $2D04C
  //   第3段: 设置 $5DF40 参数 → 队列 C9AA → 播放音效
  // ================================================================

  private _transl_C944(): void {
    switch (this.c944Step) {
      case C944_Step.INIT:
        this.ram.write16(0xFFA6D4, 0);
        this.ram.write8(0xFF816E, 0);

        this.displayQueue.clearTaskLists();
        this._transl_C7F6();

        for (let addr = 0xFF9562; addr <= 0xFF9592; addr += 2) {
          this.ram.write16(addr, 0);
        }

        this.c944Step = C944_Step.CHECK_SCENE;
        break;

      case C944_Step.CHECK_SCENE:
        this.ram.write8(0xFFAA10, 0);

        this.displayQueue.clearTaskLists();
        for (let addr = 0xFF9562; addr <= 0xFF9592; addr += 2) {
          this.ram.write16(addr, 0);
        }

        this.c944Step = C944_Step.SET_PARAMS;
        break;

      case C944_Step.SET_PARAMS:
        // ROM $33005-$33015:
        //   $33005  move.l #$5DF40,($95A2) ; 标题参数指针
        //   $33007  move.w #2,($95A6)       ; CRAM 偏移
        //   $33009  move.w #$F,($95A8)      ; 参数
        //   $33011  move.l #$C9AA,(TASK_PTR) ; 队列 C9AA
        //   $33015  jmp $85EE               ; 播放音效
        this.ram.write32(0xFF95A2, TITLE_PARAM_PTR);
        this.ram.write16(0xFF95A6, 2);
        this.ram.write16(0xFF95A8, 0x0F);

        this.displayQueue.pushSceneCramUpdate();

        this.ram.write32(RAM_VARS.TASK_PTR, TASK_C9AA);
        this.runningTask = TASK_C9AA;
        this.c9aaStep = C9AA_Step.INIT;

        this.c944Step = C944_Step.DONE;
        console.log('[TaskSystem] C944完成: 标题参数设置 → C9AA');
        break;
    }
  }

  // ================================================================
  // C7F6: VDP 窗口/显示初始化
  // ================================================================

  private _transl_C7F6(): void {
    // ★ 窗口位置 (ROM 原文: move.l #$94009E,($C00004) → reg $11=$94, $12=$9E)
    this.vdp.writeRegister(0x11, 0x94);  // 窗口 H 位置 (off-screen → 关闭窗口层)
    this.vdp.writeRegister(0x12, 0x9E);  // 窗口 V 位置
    this.ram.write16(0xFF8172, 0);

    // ★★★ VDP 显示基础设置 (ROM $C7F6 通过后续场景系统间接配置, 此处直接设置) ★★★

    // Reg $01: 显示使能 + VInt 使能 + DMA 使能 (没有 HV 锁)
    //   bit 7 = 0 (TMS9918 兼容禁用)
    //   bit 6 = 1 (★ Display enable — 不设等于黑屏)
    //   bit 5 = 1 (VInt enable)
    //   bit 4 = 1 (DMA enable)
    //   bit 2 = 0 (NTSC/PAL: 0=NTSC)
    this.vdp.writeRegister(0x01, 0x74);  // $74 = 0111_0100

    // Reg $0C: H40 模式 (320px)
    //   bit 0 = 1 (H40: 320px wide)
    this.vdp.writeRegister(0x0C, 0x81);  // $81 = 1000_0001 (H40 + shadow/highlight off + external interrupt)

    // Reg $02: Plane A nametable @ VRAM $C000 (128 cells × 2 bytes = $100)
    //   VRAM $C000 = $30 × $400
    this.vdp.writeRegister(0x02, 0x30);

    // Reg $04: Plane B nametable @ VRAM $E000 (7 × $2000 per Genesis VDP spec)
    //   用于标题画面底部菜单/文字层
    this.vdp.writeRegister(0x04, 0x07);

    // Reg $0D: HScroll table @ VRAM $FC00
    this.vdp.writeRegister(0x0D, 0x3F);

    // Reg $0F: VRAM 自动增量 = $02 (word step)
    this.vdp.writeRegister(0x0F, 0x02);

    // Reg $07: 背景色 = CRAM index 0 (palette 0, color 0)
    this.vdp.writeRegister(0x07, 0x00);

    // Reg $10: Plane 尺寸 — Plane A = 64×32 cells, Plane B = 64×32 cells
    //   bits[1:0]=01 (A=64), bits[5:4]=01 (B=64)
    this.vdp.writeRegister(0x10, 0x05);
  }

  // ================================================================
  // CRAM 标题画面调色板 (从 ROM 预提取数据加载)
  //
  // TITLE_CRAM: 128 bytes = 64 colors × 2 bytes each (big-endian Genesis 9-bit RGB)
  // ================================================================

  private _loadTitleCram(): void {
    const colors: number[] = [];
    for (let i = 0; i < 64; i++) {
      const lo = TITLE_CRAM[i * 2];      // 低位字节 (数据为 [lo, hi] 小端序)
      const hi = TITLE_CRAM[i * 2 + 1];  // 高位字节
      colors.push(((hi << 8) | lo) & 0x80FF); // 保留 game-old CRAM 格式 (byte0 全部 + byte1 bit7)
    }
    this.vdp.cram.loadColors(colors);
    console.log('[TaskSystem] CRAM 标题画面调色板已加载 (64 色, 来自 TitleScreenData)');
  }

  // ================================================================
  // Nametable 初始化 (使用 ROM 预提取标题画面数据)
  // ================================================================

  /**
   * 初始化 Plane A nametable (VRAM $C000, 64×32 cells = 4096 bytes)
   * 直接写入 TITLE_NAMETABLE_A 预提取数据 (Genesis big-endian word pairs)
   */
  private _initPlaneANametable(): void {
    const VRAM_PLANE_A = 0xC000;
    this.vdp.vram.writeBytes(VRAM_PLANE_A, TITLE_NAMETABLE_A);
    console.log(`[TaskSystem]   Plane A nametable 已加载 (${TITLE_NAMETABLE_A.length} bytes, 来自 TitleScreenData)`);
  }

  /**
   * 初始化 Plane B nametable
   * 写入地址由 VDP reg $04 决定: reg * $2000
   * reg $04=0x07 → VRAM $E000 (Genesis VDP 规格)
   * 数据大小: 64×32 cells × 2 bytes = 4096 bytes
   *
   * BUG FIX: 之前写 0x1C00 (错误使用 *$400),
   * 虽读写地址一致未造成渲染异常, 但偏离 Genesis 硬件规格
   */
  private _initPlaneBNametable(): void {
    const VRAM_PLANE_B = this.vdp.reg[0x04] * 0x2000;
    this.vdp.vram.writeBytes(VRAM_PLANE_B, TITLE_NAMETABLE_B);
    console.log(`[TaskSystem]   Plane B nametable 已加载 → VRAM $${VRAM_PLANE_B.toString(16)} (${TITLE_NAMETABLE_B.length} bytes)`);
  }

  // ================================================================
  // C9AA: 场景过渡 → 加载资源 → 标题循环 → Demo → 部署
  //
  // ROM (M68K 验证):
  //   $C9AA: jsr $C7F6
  //   $C9B0: clr.w ($FFFFA6DC)
  //   $C9B6: clr.b ($FFFFF350)
  //   $C9BC: tst.w ($FF78FA)           ★ 关键分流点
  //   $C9C2: bne.w $CA3C                ; FF78FA!=0 → DEMO
  //   $C9C6: jsr $9EC4 → $C91E → ...   ; FF78FA==0 → 正常标题
  //   $CA3C: ...                         ★ Demo 入口
  // ================================================================

  private _transl_C9AA(): void {
    switch (this.c9aaStep) {
      case C9AA_Step.INIT: {
        // step 0: 显示初始化 + 清标志 + 加载调色板 + ★ 检查 $FF78FA 分流
        // ROM: $C9AA jsr $C7F6 → $C9B0 clr.w $A6DC → $C9B6 clr.b $F350
        //      $C9BC tst.w ($FF78FA) → $C9C2 bne.w $CA3C
        this._transl_C7F6();
        this.ram.write16(0xFFA6DC, 0);
        this.ram.write8(0xFFF350, 0);

        // ★ 加载标题画面 CRAM 调色板 (ROM 中由场景系统/显示列表间接加载)
        this._loadTitleCram();

        // ★ ROM $C9BC: tst.w ($FF78FA); bne.w $CA3C
        const demoFlag = this.ram.read16(RAM_DEMO_FLAG);
        console.log(
          `[TaskSystem] C9AA: $FF78FA=0x${demoFlag.toString(16).padStart(4, '0').toUpperCase()} ` +
          `→ ${demoFlag !== 0 ? '开场动画/Demo 模式 ($CA3C)' : '正常标题画面'} (帧${this.frameCount})`
        );

        if (demoFlag !== 0) {
          // ★ $FF78FA != 0 → 跳转 $CA3C (开场动画 / Demo)
          this.inDemoMode = true;
          this.c9aaStep = C9AA_Step.DEMO_LOAD;
        } else {
          // $FF78FA == 0 → $C9C6 正常标题画面路径 (加载资源)
          this.c9aaStep = C9AA_Step.LOAD_RESOURCE;
        }
        break;
      }

      case C9AA_Step.LOAD_RESOURCE: {
        // ★ ROM $33029 (路径A: 正常标题 → 加载资源):
        //   jsr $C91E: $32963-$32973  读资源 8001, 解压到 VRAM $0000
        //   ROM $32966  move.w #$8001,d0
        //   ROM $32968  movea.w #0,a1
        //   ROM $32970  jsr $99B2         ; 解压加载
        //
        // ★★ BUG FIX: 标题画面 tiles 来自三个 ROM 资源:
        //   Entry 1 (0x8001) + Entry 3 (0x8003) + Entry 8 (0x8008)
        //   (来源: game-old/data/TitleScreenData.ts 注释:
        //    "TILES: ROM Entry 1 (0x8001) + Entry 3 (0x8003) + Entry 8 (0x8008)")
        //   0x8001 仅提供 256 tiles, nametable 引用了 tile 272, 304 等 →
        //   必须三者全部加载, 拼接在 VRAM 中。
        const TITLE_RESOURCES = [0x8001, 0x8003, 0x8008];
        const RAM_DECOMP_BUF = 0xFFFF1000;
        let totalTilesBytes = 0;
        let vramAddr = 0x0000;
        let allOk = true;

        for (const resId of TITLE_RESOURCES) {
          const decompSize = this.decompressResource(resId, RAM_DECOMP_BUF);
          if (decompSize > 0) {
            const srcBuffer = this.ram.readBytes(RAM_DECOMP_BUF, decompSize);
            this.vdp.vram.writeBytes(vramAddr, srcBuffer);
            const tileCount = decompSize >> 5;
            console.log(
              `[TaskSystem]   资源 $${resId.toString(16)} → VRAM $${vramAddr.toString(16)}: ` +
              `${decompSize} bytes (${tileCount} tiles, idx ${totalTilesBytes>>5}-${(totalTilesBytes+decompSize)>>5})`
            );
            vramAddr += decompSize;
            totalTilesBytes += decompSize;
          } else {
            console.warn(`[TaskSystem]   资源 $${resId.toString(16)} 解压失败!`);
            allOk = false;
            break;
          }
        }

        if (allOk && totalTilesBytes > 0) {
          console.log(`[TaskSystem]   标题 tiles 总加载: ${totalTilesBytes} bytes (${totalTilesBytes>>5} tiles) → VRAM \$0000-\$${(totalTilesBytes-1).toString(16)}`);

          // 清空显示队列中的残留命令
          this.displayQueue.processDisplayQueue();

          // ★ 初始化 Plane A + Plane B nametables (预提取标题画面数据)
          this._initPlaneANametable();
          this._initPlaneBNametable();
        }

        // 角色初始化
        this.c9aaStep = C9AA_Step.INIT_CHARS;
        break;
      }

      case C9AA_Step.INIT_CHARS: {
        // ★ ROM $33031-$33047 (路径A: 正常标题 → 角色初始化 → 队列 CA0A):
        //   $33031  jsr $10C1E → $1113E → $111BC → $11208 (角色初始化)
        //   $33039  bsr $CC58            (初始化)
        //   $33041  move.w #5,($A61C)    (定时器 = 5 帧)
        //   $33043  move.l #$CA0A,(TASK_PTR) (队列 CA0A 标题菜单)
        //   $33047  jmp $85EE            (播放音效)
        //
        // NOTE: ROM 原文走 CA0A→CAF2(部署), 此处我们跳入 TITLE_LOOP
        //       因为 TS 端用空闲倒计时 + 输入检测取代 ROM 任务链自动推进。
        this.ram.write16(RAM_VARS.GAME_PHASE, 1);

        // ★ 重置 demo 相关状态, 进入标题空闲循环
        this.titleIdleFrames = 0;
        this.inDemoMode = false;
        this.lastInputValue = 0;

        console.log('[TaskSystem] C9AA: 标题画面初始化完成 → 进入 TITLE_LOOP (含 demo 倒计时)');
        this.c9aaStep = C9AA_Step.TITLE_LOOP;
        break;
      }

      case C9AA_Step.TITLE_LOOP: {
        // ★ 标题画面空闲循环 (含 demo 倒计时 + ★ 输入检测 → 菜单)
        // ROM 原文: C9C6→...→CA0A→CAF2 自动走过, 不存在空闲循环
        // TS 端加入: 空闲倒计时 + 输入检测 + $FF78FA 标志跟踪

        // 跟踪 $FF78FA 变化
        this._trackDemoFlag();

        // 先检查当前是否已在 demo 路径
        const demoFlag = this.ram.read16(RAM_DEMO_FLAG);
        if (demoFlag !== 0 && !this.inDemoMode) {
          console.log(`[Demo] ★ 检测到 $FF78FA=0xFFFF → 激活 Demo 模式 (帧${this.frameCount})`);
          this.inDemoMode = true;
          this.c9aaStep = C9AA_Step.DEMO_LOAD;
          this.demoFrameCount = 0;
          return;
        }

        if (this.inDemoMode) {
          if (this._hasNewInput()) {
            console.log(`[Demo] 检测到输入 → 退出 Demo, 返回标题 (帧${this.frameCount})`);
            this.inDemoMode = false;
            this.titleIdleFrames = 0;
            this.ram.write16(RAM_DEMO_FLAG, 0x0000);
            this.c9aaStep = C9AA_Step.LOAD_RESOURCE;
            return;
          }
          this.c9aaStep = C9AA_Step.DEMO_LOOP;
          return;
        }

        // ★ 检测输入 → 进入标题菜单
        const joypad = this._readJoypadState();
        const hadInput = (joypad !== 0 && joypad !== this.lastInputValue);
        this.lastInputValue = joypad;

        if (hadInput) {
          console.log(
            `[Menu] ★ 检测到输入 (joypad=0x${joypad.toString(16).padStart(2, '0')}) → 进入标题菜单 (帧${this.frameCount})`
          );
          this.titleIdleFrames = 0;
          this.ram.write16(RAM_DEMO_FLAG, 0x0000);
          // ★ 转菜单
          this.titleMenuCursorIndex = 0;
          this.titleMenuBlinkFrame = 0;
          this.titleMenuSetupDone = false;
          this.c9aaStep = C9AA_Step.TITLE_MENU;
          return;
        }

        // 无输入 → 递增空闲倒计时
        this.titleIdleFrames++;
        if (this.titleIdleFrames >= TITLE_IDLE_TIMEOUT) {
          console.log(
            `[Demo] ★ 空闲 ${TITLE_IDLE_TIMEOUT} 帧 (~${Math.round(TITLE_IDLE_TIMEOUT / 60)}秒) 无输入 → 进入 Demo! (帧${this.frameCount})`
          );
          this.ram.write16(RAM_DEMO_FLAG, 0xFFFF);
          this.titleIdleFrames = 0;
          return;
        }

        if (this.titleIdleFrames % TITLE_LOG_INTERVAL === 0 && this.titleIdleFrames > 0) {
          const secondsLeft = Math.round((TITLE_IDLE_TIMEOUT - this.titleIdleFrames) / 60);
          console.log(
            `[Demo] 空闲计时: ${this.titleIdleFrames}/${TITLE_IDLE_TIMEOUT} 帧 (${Math.round(this.titleIdleFrames/60)}s / 约${secondsLeft}s后进入demo)`
          );
        }
        break;
      }

      case C9AA_Step.TITLE_MENU: {
        // ★ 标题菜单 — 光标移动 + 选择
        // ROM 等效: $CA0A (jsr $11DA4 获取选择 → 读取角色坐标 → $CAF2 部署)
        this._handleTitleMenu();
        break;
      }

      case C9AA_Step.DEMO_LOAD: {
        // ★ ROM $33075-$33094 (路径B: Demo/开场动画 → 加载场景):
        //   $33076  move.l #$00010001,($A6DE) ; demo 坐标 (1,1)
        //   $33078  jsr $9EC4            ; 加载场景
        //   $33080  jsr $FA38            ; 加载 demo 场景数据
        //   $33082  bsr $10C4C           ; 角色初始化
        //   $33084  jsr $14D8C           ; 初始化
        //   $33086  bsr $9172            ; 显示列表初始化
        //   $33088  bsr $9020            ; 输入状态初始化
        //   $33090  jsr $C7B8            ; 过渡效果
        //   $33092  move.l #$CA72,(TASK_PTR) ; 队列 CA72 demo 循环
        //
        // ★ ROM $CA72: 加载资源 $8001 → VRAM $0000 (与正常标题共享同一套 tile)
        //   $33096  move.w #$8001,d0
        //   $33100  jsr $99B2           ; 解压加载

        console.log(`[Demo] ★ 加载开场动画/Demo 场景 (ROM $CA3C) (帧${this.frameCount})`);

        // ★ 加载 Demo 所用标题 tiles: 0x8001 + 0x8003 + 0x8008 → 拼接在 VRAM
        const TITLE_RESOURCES = [0x8001, 0x8003, 0x8008];
        const RAM_DECOMP_BUF = 0xFFFF1000;
        let totalTilesBytes = 0;
        let vramAddr = 0x0000;

        for (const resId of TITLE_RESOURCES) {
          const decompSize = this.decompressResource(resId, RAM_DECOMP_BUF);
          if (decompSize > 0) {
            const srcBuffer = this.ram.readBytes(RAM_DECOMP_BUF, decompSize);
            this.vdp.vram.writeBytes(vramAddr, srcBuffer);
            vramAddr += decompSize;
            totalTilesBytes += decompSize;
          } else {
            console.warn(`[Demo]   资源 $${resId.toString(16)} 解压失败!`);
          }
        }
        console.log(`[Demo]   标题 tiles 加载: ${totalTilesBytes} bytes → VRAM \$0000-\$${(totalTilesBytes-1).toString(16)}`);
        this.displayQueue.processDisplayQueue();

        // 设置 demo 坐标 (1,1) — ROM $CA3C
        this.ram.write32(0xFFA6DE, 0x00010001);
        // 设置 demo 阶段
        this.ram.write16(RAM_VARS.GAME_PHASE, 6);

        this.demoFrameCount = 0;
        this.c9aaStep = C9AA_Step.DEMO_LOOP;
        break;
      }

      case C9AA_Step.DEMO_LOOP: {
        // ★ ROM $33096-$33148 (CA72+CA94+CAA8: demo 循环 → CAF2 部署):
        //   $33096  move.w #$8001,d0       ; 加载资源
        //   $33100  jsr $99B2              ; 解压
        //   $33102  move.l #$CA94,(TASK_PTR); 队列 CA94
        //   $33108  move.l #$CAA8,(TASK_PTR); 队列 CAA8
        //   $33114-$33148  角色遍历 + 坐标设置 → CAF2(部署)
        //
        // NOTE: ROM 中 demo 循环会自然走到 CAF2 部署阶段
        //       TS 端加入输入检测和超时机制, 按键/超时 → 返回 LOAD_RESOURCE(正常标题)

        this.demoFrameCount++;

        // 检查输入 → 退出 demo
        if (this._hasNewInput()) {
          console.log(
            `[Demo] ★ 检测到输入 → 退出 Demo 模式 ` +
            `(joypad=0x${this.lastInputValue.toString(16).padStart(2, '0')}, 帧${this.frameCount})`
          );
          this.inDemoMode = false;
          this.titleIdleFrames = 0;
          this.ram.write16(RAM_DEMO_FLAG, 0x0000);
          // ★ 走正常标题初始化路径 (ROM $C9C6→LOAD_RESOURCE→INIT_CHARS→TITLE_LOOP)
          this.c9aaStep = C9AA_Step.LOAD_RESOURCE;
          return;
        }

        // demo 状态日志
        if (this.demoFrameCount % 120 === 0) {
          console.log(
            `[Demo] ★ Demo 播放中... 帧${this.demoFrameCount} ` +
            `(joypad=$FF8179=0x${this._readJoypadState().toString(16).padStart(2, '0')}) ` +
            `(按任意键退出, 帧${this.frameCount})`
          );
        }

        // demo 自动循环超时 (~60秒 = 3600帧) 后自动返回标题
        if (this.demoFrameCount > 3600) {
          console.log(`[Demo] Demo 超时 → 返回标题画面 (帧${this.frameCount})`);
          this.inDemoMode = false;
          this.titleIdleFrames = 0;
          this.ram.write16(RAM_DEMO_FLAG, 0x0000);
          // ★ 超时也走正常标题初始化
          this.c9aaStep = C9AA_Step.LOAD_RESOURCE;
          return;
        }
        break;
      }

      case C9AA_Step.SET_PHASE: {
        // step N: ★ 设置游戏阶段 = 5 (部署)
        this.ram.write16(RAM_VARS.GAME_PHASE, 5);

        this.ram.write32(RAM_VARS.TASK_PTR, TASK_CA00);
        this.runningTask = TASK_CA00;

        this.c9aaStep = C9AA_Step.DONE;
        console.log('[TaskSystem] C9AA完成: 阶段=5(部署) → CA00');
        break;
      }
    }
  }

  // ================================================================
  // CA00: 部署主循环
  // ROM 等价: $CAF2 ($33150+) 是 CA0A 和 CAA8 的汇聚点, 进入部署阶段
  // ================================================================

  private _transl_CA00(): void {
    if (this.frameCount % 60 === 0) {
      const phase = this.ram.read16(RAM_VARS.GAME_PHASE);
      const dqState = this.displayQueue.dumpQueueState();
      console.log(`[TaskSystem] CA00 (部署循环) 帧${this.frameCount} 阶段=${phase} ${dqState}`);
    }
  }

  // ============================================================
  // 状态查询
  // ============================================================

  get currentPhase(): number        { return this.phase; }
  get frames(): number              { return this.frameCount; }
  get isBooted(): boolean           { return this.phase >= Phase.RUNNING; }
  get currentRunningTask(): number  { return this.runningTask; }
}
