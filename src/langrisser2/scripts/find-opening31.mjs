const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROM_PATH = path.join(__dirname, '../20260713/Langrisser II (Japan)_68K-gens-rom-dump.bin');
const RAM_PATH = path.join(__dirname, '../20260713/Langrisser II (Japan)_68K-gens-ram-dump.ram');
const rom = fs.readFileSync(ROM_PATH);
const ram = fs.readFileSync(RAM_PATH);

function rb(o) { return rom[o] & 0xff; }
function rw(o) { return ((rom[o] & 0xff) << 8) | (rom[o + 1] & 0xff); }
function rl(o) { return (((rom[o] & 0xff) << 24) | ((rom[o + 1] & 0xff) << 16) | ((rom[o + 2] & 0xff) << 8) | (rom[o + 3] & 0xff)) >>> 0; }
function sx16(v) { return v > 0x7FFF ? v - 0x10000 : v; }
function addrW(w) { return ((w | 0xFFFF0000) >>> 0); }

// RAM 读取函数 (0xFF0000 基地址)
function ramRb(addr) {
  const offset = addr - 0xFF0000;
  if (offset < 0 || offset >= ram.length) return 0;
  return ram[offset] & 0xff;
}
function ramRw(addr) {
  return ((ramRb(addr) << 8) | ramRb(addr + 1)) & 0xffff;
}
function ramRl(addr) {
  return (((ramRb(addr) << 24) | (ramRb(addr + 1) << 16) | (ramRb(addr + 2) << 8) | ramRb(addr + 3)) >>> 0);
}

