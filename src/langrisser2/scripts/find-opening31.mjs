  const entryAddr = TABLE_ADDR + i * ENTRY_SIZE;
  const byte0 = ramRb(entryAddr);
  const byte1 = ramRb(entryAddr + 1);
  
  if (byte0 === 0 && byte1 === 0) continue; // 空条目
  
  console.log(`条目 [${i}] @ 0x${entryAddr.toString(16)}:`);
  
  // 显示前 32 字节
